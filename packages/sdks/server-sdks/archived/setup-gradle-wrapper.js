// Download gradle-wrapper.jar
const jarUrl = 'https://services.gradle.org/distributions/gradle-8.5-bin.zip';
// Instead of downloading the full ZIP, let's get the wrapper jar directly from GitHub
const wrapperJarUrl = 'https://raw.githubusercontent.com/gradle/gradle/v8.5.0/gradle/wrapper/gradle-wrapper.jar';
const jarPath = path.join(wrapperDir, 'gradle-wrapper.jar');

function download(url, dest) {
  return new Promise((resolve, reject) => {
    const file = fs.createWriteStream(dest);
    const protocol = url.startsWith('https') ? https : http;
    protocol.get(url, (response) => {
      // Handle redirects
      if (response.statusCode === 301 || response.statusCode === 302) {
        download(response.headers.location, dest).then(resolve).catch(reject);
        return;
      }
      response.pipe(file);
      file.on('finish', () => {
        file.close();
        resolve();
      });
    }).on('error', (err) => {
      fs.unlink(dest, () => {});
      reject(err);
    });
  });
}

download(wrapperJarUrl, jarPath)
