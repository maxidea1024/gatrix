/**
 * ISO 3166-1 alpha-2 country data
 * Contains country code, English name, and native name
 */
export interface Country {
  code: string; // ISO 3166-1 alpha-2 (lowercase)
  name: string; // English name
  native: string; // Native name
}

/**
 * Get flag image URL from flagcdn.com
 * flagcdn.com only supports specific widths: 20, 40, 80, 160, 320, 640
 * @param countryCode ISO 3166-1 alpha-2 code (case insensitive)
 * @param displaySize Display size in pixels - will be mapped to nearest supported CDN width
 */
const SUPPORTED_WIDTHS = [20, 40, 80, 160, 320, 640];

export const getFlagUrl = (countryCode: string, displaySize = 20): string => {
  // Pick the smallest supported width that is >= displaySize for crisp rendering
  const cdnWidth = SUPPORTED_WIDTHS.find((w) => w >= displaySize) || 640;
  return `https://flagcdn.com/w${cdnWidth}/${countryCode.toLowerCase()}.png`;
};

